//演绎转移符号的使用
public class November4th{

	//编写一个main方法
	public static void main(String[] args) {

           // \t：对齐
		System.out .println("北京\t天津\t上海");
		  // \n：换行
		   System.out.println("我不是\n你爹");
		   // \\：一个\
		   System.out.println("C:\\Windows\\System32\\cmd.exe");
		   //如果要两个\\,就是\\\\

		   // \"：一个"
		   System.out.println("我说\"我不是你爸爸\"");
		   //中文单双引号不用\
		   System.out.println("“就是打开”");

		   // \'：一个'

		   // \r：一个回车(合并)
		   System.out.println("韩顺平教育\r北京");//输出 北京平教育
		   System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");//作业

			  
	}

}